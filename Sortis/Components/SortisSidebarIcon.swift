private struct SortisSidebarPathShape: Shape {
    let pathData: String

    func path(in rect: CGRect) -> Path {
        let sourcePath = SortisSVGPathParser.parse(pathData)
        let scale = min(rect.width, rect.height) / 24.0
        let xOffset = rect.minX + ((rect.width - (24.0 * scale)) / 2)
        let yOffset = rect.minY + ((rect.height - (24.0 * scale)) / 2)
        let scaledPath = sourcePath.applying(CGAffineTransform(scaleX: scale, y: scale))
        return scaledPath.applying(CGAffineTransform(translationX: xOffset, y: yOffset))
    }
}

private enum SortisSVGPathToken {
    case command(Character)
    case number(CGFloat)
}

private enum SortisSVGPathParser {
    static func parse(_ pathData: String) -> Path {
        let tokens = tokenize(pathData)
        var index = 0
        var command: Character?
        var path = Path()
        var currentPoint = CGPoint.zero
        var subpathStart = CGPoint.zero

        func hasNumber() -> Bool {
            guard index < tokens.count else { return false }
            if case .number = tokens[index] {
                return true
            }
            return false
        }

        func readNumber() -> CGFloat {
            guard index < tokens.count else { return 0 }
            defer { index += 1 }
            if case let .number(value) = tokens[index] {
                return value
            }
            return 0
        }

        while index < tokens.count {
            if case let .command(nextCommand) = tokens[index] {
                command = nextCommand
                index += 1
            }

            guard let activeCommand = command else { break }

            switch activeCommand {
            case "M":
                guard hasNumber() else { continue }
                let point = CGPoint(x: readNumber(), y: readNumber())
                path.move(to: point)
                currentPoint = point
                subpathStart = point

                while hasNumber() {
                    let nextPoint = CGPoint(x: readNumber(), y: readNumber())
                    path.addLine(to: nextPoint)
                    currentPoint = nextPoint
                }
            case "L":
                while hasNumber() {
                    let point = CGPoint(x: readNumber(), y: readNumber())
                    path.addLine(to: point)
                    currentPoint = point
                }
            case "H":
                while hasNumber() {
                    let point = CGPoint(x: readNumber(), y: currentPoint.y)
                    path.addLine(to: point)
                    currentPoint = point
                }
            case "V":
                while hasNumber() {
                    let point = CGPoint(x: currentPoint.x, y: readNumber())
                    path.addLine(to: point)
                    currentPoint = point
                }
            case "C":
                while hasNumber() {
                    let control1 = CGPoint(x: readNumber(), y: readNumber())
                    let control2 = CGPoint(x: readNumber(), y: readNumber())
                    let point = CGPoint(x: readNumber(), y: readNumber())
                    path.addCurve(to: point, control1: control1, control2: control2)
                    currentPoint = point
                }
            case "Z", "z":
                path.closeSubpath()
                currentPoint = subpathStart
            default:
                break
            }
        }

        return path
    }

    private static func tokenize(_ source: String) -> [SortisSVGPathToken] {
        var tokens: [SortisSVGPathToken] = []
        var buffer = ""

        func flush() {
            guard !buffer.isEmpty, let value = Double(buffer) else {
                buffer = ""
                return
            }
            tokens.append(.number(CGFloat(value)))
            buffer = ""
        }

        for character in source {
            if character.isLetter {
                flush()
                tokens.append(.command(character))
            } else if character.isNumber || character == "." {
                buffer.append(character)
            } else if character == "-" {
                flush()
                buffer.append(character)
            } else {
                flush()
            }
        }

        flush()
        return tokens
    }
}
